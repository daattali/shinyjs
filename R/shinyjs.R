#' shinyjs
#'
#' Perform common JavaScript operations in Shiny apps using plain R code.
#'
#' \code{shinyjs} lets you perform common JavaScript operations in Shiny
#' applications without having to know any JavaScript. Many useful JavaScript
#' functions are made available by shinyjs with a simple R interface so that
#' you don't have to write any JavaScript code. Examples include hiding an
#' element, disabling an input, resetting an input back to its original value,
#' and many more useful functions. Even if you do know JavaScript,
#' shinyjs can be used as convenience functions to avoid dealing with message
#' passing and writing JavaScript code.
#'
#' View a \href{http://daattali.com/shiny/shinyjs-demo/}{demo Shiny app}
#' or see the full \href{https://github.com/daattali/shinyjs}{README} on GitHub.
#'
#' @docType package
#' @name shinyjs
